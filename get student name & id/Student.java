public class Student {
    private String name;
    private int id;

    public Student() {
        this.name = "Sohel Rana";
        this.id = 57;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }
}